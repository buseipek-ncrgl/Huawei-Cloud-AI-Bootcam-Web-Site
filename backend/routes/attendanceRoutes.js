router.post('/:week', authenticate, async (req, res) => {
  try {
    const weekNum = Number(req.params.week);
    const dayNum = Number(req.body.day);

    if (![1, 2].includes(dayNum)) {
      return res.status(400).json({ success: false, error: 'G√ºn 1 veya 2 olmalƒ±' });
    }

    // Aktif g√ºn kontrol√º
    const activeSession = await Session.findOne({ week: weekNum });

    if (
      !activeSession ||
      !activeSession.activeDays ||
      !activeSession.activeDays[`day${dayNum}`]
    ) {
      return res.status(400).json({ success: false, error: 'Bu g√ºn i√ßin yoklama alƒ±nmƒ±yor' });
    }

    // ‚ùó Yalnƒ±zca g√ºncellenecek alanlarƒ± set ediyoruz (unique alanlarƒ± deƒüil!)
   const attendance = await Attendance.findOneAndUpdate(
  { userId: req.user.id, week: weekNum, day: dayNum },
  {
    $set: {
      attended: true,
      timestamp: new Date()
    }
  },
  {
    upsert: true,
    new: true,
    setDefaultsOnInsert: true
  }
);



    return res.json({
      success: true,
      message: `${weekNum}. hafta ${dayNum}. g√ºn katƒ±lƒ±mƒ± kaydedildi`,
      attendance
    });
  } catch (err) {
    console.error('‚ùå Katƒ±lƒ±m kaydedilemedi:', err.message);
    console.error(err.stack);
    return res.status(500).json({ success: false, error: 'Katƒ±lƒ±m kaydedilemedi' });
  }
});


